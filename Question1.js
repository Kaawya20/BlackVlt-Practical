//Print numbers from 1 to 100 to the console, but for multiples of three, print "Fizz", for multiples of five, print "Buzz" and for numbers which are both multiples of three and five, print "FizzBuzz".


for ( var i = 1; i <= 100; i++ )
{
  if ( i%3 === 0 && i%5 === 0 )
  {
    console.log( i + " FizzBuzz" );
  }
  else if ( i%3 === 0 ) 
  {
    console.log(i+ " Fizz" );
  }
  else if ( i%5 === 0 ) 
  {
    console.log(i+ " Buzz" );
  }
  else
  {
    console.log(i);
  }
}